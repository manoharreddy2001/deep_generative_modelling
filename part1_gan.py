def generate_gaussian_mixture(n_samples=2000):
    centers = [
        (-2, -2),
        (2, -2),
        (-2, 2),
        (2, 2)
    ]

    data = []
    for _ in range(n_samples):
        cx, cy = centers[np.random.randint(0, len(centers))]
        x = np.random.normal(cx, 0.3)
        y = np.random.normal(cy, 0.3)
        data.append([x, y])

    return np.array(data, dtype=np.float32)


