def train_dcgan(
    train_loader,
    latent_dim=100,
    lr=0.0002,
    epochs=10,
    sample_interval=1,
    run_name="default_run"
):
    generator = Generator(latent_dim=latent_dim, img_channels=3).to(device)
    discriminator = Discriminator(img_channels=3).to(device)

    criterion = nn.BCELoss()

    optimizer_G = optim.Adam(generator.parameters(), lr=lr, betas=(0.5, 0.999))
    optimizer_D = optim.Adam(discriminator.parameters(), lr=lr, betas=(0.5, 0.999))

    g_losses = []
    d_losses = []

    run_dir = os.path.join("bloodmnist_outputs", run_name)
    os.makedirs(run_dir, exist_ok=True)
    epoch_img_dir = os.path.join(run_dir, "generated_epochs")
    os.makedirs(epoch_img_dir, exist_ok=True)

    print(f"\nStarting run: {run_name}")
    print(f"Learning rate: {lr}, Epochs: {epochs}")

    for epoch in range(epochs):
        epoch_g_loss = 0.0
        epoch_d_loss = 0.0

        for real_imgs, _ in tqdm(train_loader, desc=f"{run_name} | Epoch {epoch+1}/{epochs}", leave=False):
            real_imgs = real_imgs.to(device, non_blocking=True)
            batch_size_curr = real_imgs.size(0)

            real_labels = torch.ones(batch_size_curr, 1, device=device)
            fake_labels = torch.zeros(batch_size_curr, 1, device=device)

            # ---------------------
            # Train Discriminator
            # ---------------------
            optimizer_D.zero_grad()

            real_output = discriminator(real_imgs)
            d_real_loss = criterion(real_output, real_labels)

            z = torch.randn(batch_size_curr, latent_dim, device=device)
            fake_imgs = generator(z)
            fake_output = discriminator(fake_imgs.detach())
            d_fake_loss = criterion(fake_output, fake_labels)

            d_loss = d_real_loss + d_fake_loss
            d_loss.backward()
            optimizer_D.step()

            # -----------------
            # Train Generator
            # -----------------
            optimizer_G.zero_grad()

            z = torch.randn(batch_size_curr, latent_dim, device=device)
            gen_imgs = generator(z)
            output = discriminator(gen_imgs)

            g_loss = criterion(output, real_labels)
            g_loss.backward()
            optimizer_G.step()

            epoch_d_loss += d_loss.item()
            epoch_g_loss += g_loss.item()

        avg_d_loss = epoch_d_loss / len(train_loader)
        avg_g_loss = epoch_g_loss / len(train_loader)

        d_losses.append(avg_d_loss)
        g_losses.append(avg_g_loss)

        print(f"Epoch [{epoch+1}/{epochs}] | D Loss: {avg_d_loss:.4f} | G Loss: {avg_g_loss:.4f}")

        if (epoch + 1) % sample_interval == 0:
            save_generated_grid(generator, latent_dim, epoch + 1, epoch_img_dir, device)

    # Save final models
    torch.save(generator.state_dict(), os.path.join(run_dir, "generator.pth"))
    torch.save(discriminator.state_dict(), os.path.join(run_dir, "discriminator.pth"))

    return generator, discriminator, g_losses, d_losses, run_dir

